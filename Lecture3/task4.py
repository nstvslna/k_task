# Зчитуємо координати першої клітини
x1 = int(input("Введіть номер стовпця першої клітини (від 1 до 8): "))
y1 = int(input("Введіть номер рядка першої клітини (від 1 до 8): "))

# Зчитуємо координати другої клітини
x2 = int(input("Введіть номер стовпця другої клітини (від 1 до 8): "))
y2 = int(input("Введіть номер рядка другої клітини (від 1 до 8): "))

# Перевіряємо, чи може тура потрапити з першої клітини на другу
if abs(x2 - x1) == abs(y2 - y1):
    print("Так")
else:
    print("Ні")
